#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	track;

	track = 0;
	while (s[track] != '\0')
	{
		write(fd, &s[track], 1);
		track++;
	}
	write(fd, "\n", 1);
}
